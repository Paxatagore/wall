<h4>Hébergement</h4>
<div class="information">
	<div>Il est convenu que chacun ou chaque famille s’occupera de son hébergement. Il y a toute l’offre souhaitée dans la région, depuis l’hôtel, les chambres d’hôtes, les campings jusqu’aux gîtes familiaux (à la semaine).</div>
	<div>Le Petit Roc est à 6 km d'Issigeac. La région autour d’Issigeac offre énormément de possibilités de tous genres qui sont très bien présentées sur les sites officiels : hôtels ou  chambres d’hôtes si vous ne faites que passer, ou gîtes pour une semaine (toujours de samedi à samedi) pour profiter de la région. En effet, Issigeac est une petite ville est au cœur du "pays des Bastides" ("villes nouvelles" du Moyen-Âge). <a href="http://www.dailymotion.com/video/x1lcejj_lot-et-garonne-dordogne-le-pays-des-bastides_tv">En cliquant sur ce lien</a>, vous accéderez à un reportage qui vous donnera bien envie de découvrir la région…). </div>
	<div>Issigeac étant juste à la frontière entre le département de la DORDOGNE et le département du LOT et GARONNE, vous pouvez chercher un hébergement des deux côtés : </div>
	<div>- au sud de la DORDOGNE, le "Pays des Bastides", 9 bastides se sont regroupées pour promouvoir ce patrimoine particulier. Vous pouvez donc commencer à chercher un logement <a href="http://www.pays-des-bastides.com/">sur le site du Pays des Bastides</a>. De là, vous serez vite en route avec plus qu’une envie : y être !</div>
	<div>- au nord du LOT et GARONNE, « Bastides en Haut Agenais » : vous trouverez des propositions d'hébergement sur les sites des offices de tourisme des différentes villes (ou villages...). </div>
